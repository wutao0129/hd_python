def main():
    conn = pymysql.connect(**DB_CONFIG)
    cursor = conn.cursor()

    updated = 0
    not_found = []

    for code, relations in TAG_RELATIONS.items():
        if relations is None:
            continue
        similar, exclusive = relations

        if similar and exclusive:
            cursor.execute(
                'UPDATE tags SET similar_tags = %s, exclusive_tags = %s WHERE code = %s',
                (similar, exclusive, code)
            )
        elif similar:
            cursor.execute(
                'UPDATE tags SET similar_tags = %s WHERE code = %s',
                (similar, code)
            )
        elif exclusive:
            cursor.execute(
                'UPDATE tags SET exclusive_tags = %s WHERE code = %s',
                (exclusive, code)
            )

        if cursor.rowcount > 0:
            updated += cursor.rowcount
        else:
            not_found.append(code)

    conn.commit()
    print(f'✅ 更新完成，共影响 {updated} 行')
    if not_found:
        print(f'⚠️ 未匹配到的 code ({len(not_found)}): {not_found}')

    # 统计
    cursor.execute('SELECT COUNT(*) FROM tags WHERE similar_tags IS NOT NULL OR exclusive_tags IS NOT NULL')
    total_with_rel = cursor.fetchone()[0]
    cursor.execute('SELECT COUNT(*) FROM tags')
    total = cursor.fetchone()[0]
    print(f'\n有相似/互斥数据的标签: {total_with_rel}/{total}')

    conn.close()


if __name__ == '__main__':
    main()
